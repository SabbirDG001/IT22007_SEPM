package Car;

public class Car {
    private String color;
    private int year;
    public String GetColor(){
        return this.color;
    }
    public int GetYear(){
        return this.year;
    }
    public void SetColor(String color){
        this.color = color;
    }
    public void SetYear(int year){
        this.year = year;
    }
}
